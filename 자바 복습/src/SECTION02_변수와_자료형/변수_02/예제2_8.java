package SECTION02_변수와_자료형.변수_02;

public class 예제2_8 {
	public static void main(String[] args) {
		//변수 선언 및 초기화
		
		String myCity = "Seoul";	//도시를 저장할 수 있는 변수를 선언하고,
									//변수에 "Seoul"을 대입(저장)했습니다.
		System.out.println("I am from " + myCity);
		
		/*
		 * TIP
		 * 자바에서 +기호는 문자열을 서로 연결하는 문자열 결합 연산자입니다.
		 * +연산자는 문자열 결합 연산자인 동시에 산술 연산자, 부호 연산자로 사용됩니다. 
		 */
	}
}
